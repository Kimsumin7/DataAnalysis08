# 변수 x에 값 3을 할당합니다
x <- 3

# 변수 y에 값 2를 할당합니다
y <- 2

# 변수 x를 변수 y로 나눈 값을 계산합니다. 결과는 1.5입니다.
x / y

# 복소수 1 + 2i를 변수 xi에 할당합니다
xi <- 1 + 2i

# 복소수 1 - 2i를 변수 yi에 할당합니다
yi <- 1 - 2i

# 변수 xi와 yi를 더합니다. 결과는 실수 부분의 합과 허수 부분의 합인 2 + 0i, 즉 2입니다.
xi + yi

# 문자열 "string"을 변수 str에 할당합니다
str <- "string"

# 변수 str의 값을 출력합니다. 결과는 "string"입니다.
str

# 논리값 TRUE를 출력합니다. R에서 TRUE는 참을 나타내는 논리 상수입니다.
TRUE

# 논리값 FALSE를 출력합니다. R에서 FALSE는 거짓을 나타내는 논리 상수입니다.
FALSE
